    private void fetchStaffData() {
        recepCount();
        drCount();
        labCount();
        pharmCount();
        try {
            ResultSet rs = null;
            Connection con = DriverManager.getConnection(url, "root", pwd);
            String query = "SELECT (SELECT count(receptionid) FROM reception) as reception, "
                    + "(SELECT count(doctorid) FROM doctor) as doctor, "
                    + "(SELECT count(labid) FROM lab) as lab, "
                    + "(SELECT count(pharmacyid) FROM pharmacy) as pharmacy";
            PreparedStatement ps = con.prepareStatement(query);
            rs = ps.executeQuery();
            if (rs.next()) {
                totalstaff.setText(rs.getString(""));
            }
        } catch (Exception ex) {}
    }

