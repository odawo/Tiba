    private void searchTextFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchTextFieldActionPerformed
        // TODO add your handling code here:
        
        try {
            Connection conn =  DriverManager.getConnection(url, "root", pwd);
            String query = "SELECT COUNT(DISTINCT patientid, diagnosis) FROM patientresult WHERE diagnosis=?";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, searchTextField.getText());
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                totalpatientslbl.setText(rs.getString("patientid"));
                totalcaseslbl.setText(rs.getString("diagnosis"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_searchTextFieldActionPerformed

